# shellcheck shell=bash
#
# passthru.updateScript for proxmox-backup-server. Refreshes pkgs/proxmox-backup-
# server/sources.json (git pins + .deb web assets + version) and regenerates the
# vendored Cargo.lock.
#
# Strategy notes:
#   * The proxmox git repos carry no usable release tags, so rather than tracking
#     master HEAD (which drifts onto unreleased code) we anchor everything to the
#     newest *released* proxmox-backup-server .deb:
#       - version    = highest proxmox-backup-server version in the apt index.
#       - proxmox-backup rev = the commit that bumped debian/changelog to exactly
#                      that .deb version (i.e. the release commit, found via -S).
#       - the four dependency repos (proxmox, proxmox-fuse, pxar, pathpatterns)
#                      = each repo's newest commit at or before the release
#                      commit's date, i.e. a coherent snapshot of the moment PBS
#                      was released.
#   * Web assets are prebuilt .debs; their URL + sha256 are taken verbatim from
#     the apt Packages index (highest version, by dpkg comparison). Assets whose
#     "source" is not "proxmox" (e.g. fonts-font-awesome from deb.debian.org) are
#     left untouched.
#   * Upstream ships no Cargo.lock, so it is regenerated by applying the
#     dependency-reroute patch and running `cargo generate-lockfile`.
#
# Env:
#   PBS_PKG_DIR        override the package directory to update.
#   SKIP_CARGO_LOCK=1  skip Cargo.lock regeneration (sources only).

# Base URL of the Proxmox apt repo (Filename fields in the index are relative).
pbs_repo_base="http://download.proxmox.com/debian/pbs"
packages_url="${pbs_repo_base}/dists/trixie/pbs-no-subscription/binary-amd64/Packages.gz"

# --- locate the package directory --------------------------------------------
pkgdir="${PBS_PKG_DIR:-}"
if [ -z "$pkgdir" ]; then
  d="$PWD"
  while [ "$d" != "/" ]; do
    if [ -f "$d/pkgs/proxmox-backup-server/sources.json" ]; then
      pkgdir="$d/pkgs/proxmox-backup-server"
      break
    fi
    if [ -f "$d/sources.json" ] && [ -f "$d/package.nix" ]; then
      pkgdir="$d"
      break
    fi
    d="$(dirname "$d")"
  done
fi
if [ -z "$pkgdir" ] || [ ! -f "$pkgdir/sources.json" ]; then
  echo "error: could not locate sources.json (set PBS_PKG_DIR)" >&2
  exit 1
fi

json="$pkgdir/sources.json"
echo "Updating $json"

workroot="$(mktemp -d)"
trap 'rm -rf "$workroot"' EXIT

# In-place jq edit of sources.json.
update_json() {
  local tmp
  tmp="$(mktemp)"
  jq "$@" "$json" >"$tmp"
  mv "$tmp" "$json"
}

# Highest-versioned stanza for a package in the apt index. Prints
# "version<TAB>filename<TAB>sha256" (the index lists every available version, in
# no particular order, so we compare with dpkg).
index_best() {
  awk -v p="Package: $1" '
    BEGIN { RS = ""; FS = "\n" }
    $1 == p {
      v = ""; f = ""; s = ""
      for (i = 1; i <= NF; i++) {
        if ($i ~ /^Version: /)       v = substr($i, 10)
        else if ($i ~ /^Filename: /) f = substr($i, 11)
        else if ($i ~ /^SHA256: /)   s = substr($i, 9)
      }
      print v "\t" f "\t" s
    }' "$packages" | {
    bv=""; bf=""; bs=""
    while IFS=$'\t' read -r v f s; do
      [ -n "$v" ] || continue
      if [ -z "$bv" ] || dpkg --compare-versions "$v" gt "$bv"; then
        bv="$v"; bf="$f"; bs="$s"
      fi
    done
    [ -n "$bv" ] && printf '%s\t%s\t%s\n' "$bv" "$bf" "$bs"
  }
}

# --- released version (from the apt Packages index) --------------------------
echo "  fetching Packages index"
packages="$workroot/Packages"
curl -fsSL "$packages_url" | gzip -d >"$packages"

IFS=$'\t' read -r deb_version _ _ < <(index_best proxmox-backup-server)
if [ -z "${deb_version:-}" ]; then
  echo "error: proxmox-backup-server not found in apt index" >&2
  exit 1
fi
# debian version -> upstream version: drop epoch (1:) and debian revision (-N).
version="${deb_version#*:}"
version="${version%-*}"
echo "  released version: $version (deb $deb_version)"

# --- proxmox-backup: the commit that released this exact .deb version ---------
# The release commit is the one that bumped debian/changelog to "$deb_version".
backup_url="$(jq -r '.repos["proxmox-backup"].url' "$json")"
echo "  resolving proxmox-backup release commit"
git clone --quiet --no-checkout "$backup_url" "$workroot/proxmox-backup.git"
backup_rev="$(git -C "$workroot/proxmox-backup.git" log -1 --format=%H \
  -S "rust-proxmox-backup ($deb_version)" -- debian/changelog)"
if [ -z "$backup_rev" ]; then
  echo "error: no commit bumps debian/changelog to $deb_version" >&2
  exit 1
fi
release_date="$(git -C "$workroot/proxmox-backup.git" log -1 --format=%cI "$backup_rev")"
echo "    $backup_rev ($release_date)"

# --- git repositories ---------------------------------------------------------
# proxmox-backup uses the release commit; the dependency repos use their newest
# commit at or before the release date. Hashes are computed with nix-prefetch-git
# against the local clone (same content -> same hash as fetchgit, no refetch),
# and the resulting clean checkout is kept for the Cargo.lock step.
declare -A repo_checkout

mapfile -t repo_keys < <(jq -r '.repos | keys[]' "$json")
for key in "${repo_keys[@]}"; do
  url="$(jq -r --arg k "$key" '.repos[$k].url' "$json")"
  name="$(jq -r --arg k "$key" '.repos[$k].name' "$json")"

  if [ "$key" = "proxmox-backup" ]; then
    clone="$workroot/proxmox-backup.git"
    rev="$backup_rev"
  else
    clone="$workroot/$name.git"
    git clone --quiet --no-checkout "$url" "$clone"
    rev="$(git -C "$clone" rev-list -1 --before="$release_date" HEAD)"
    if [ -z "$rev" ]; then
      echo "error: no $name commit at or before $release_date" >&2
      exit 1
    fi
  fi

  prefetch="$(nix-prefetch-git --quiet --url "file://$clone" --rev "$rev")"
  hash="$(jq -r '.hash' <<<"$prefetch")"
  repo_checkout["$name"]="$(jq -r '.path' <<<"$prefetch")"

  update_json --arg k "$key" --arg rev "$rev" --arg hash "$hash" \
    '.repos[$k].rev = $rev | .repos[$k].hash = $hash'
  echo "  $key -> $rev"
done

update_json --arg v "$version" '.version = $v'

# --- web assets (.debs from the Proxmox apt repo) -----------------------------
mapfile -t asset_keys < <(jq -r '.webAssets | to_entries[] | select(.value.source == "proxmox") | .key' "$json")
for name in "${asset_keys[@]}"; do
  IFS=$'\t' read -r _ best_file best_sha < <(index_best "$name")
  if [ -z "${best_file:-}" ] || [ -z "${best_sha:-}" ]; then
    echo "    warning: $name not found in index, leaving as-is" >&2
    continue
  fi
  url="${pbs_repo_base}/${best_file}"
  hash="$(nix hash convert --hash-algo sha256 --to sri "$best_sha")"
  update_json --arg k "$name" --arg url "$url" --arg hash "$hash" \
    '.webAssets[$k].url = $url | .webAssets[$k].hash = $hash'
  echo "  webAsset $name -> ${best_file##*/}"
done

# --- Cargo.lock ---------------------------------------------------------------
# Upstream ships none. Recreate the build's source layout (proxmox-backup with
# the dependency repos as siblings), apply the reroute patch, and let cargo
# resolve a fresh lockfile.
if [ "${SKIP_CARGO_LOCK:-0}" = "1" ]; then
  echo "  Cargo.lock: skipped (SKIP_CARGO_LOCK=1)"
else
  echo "  regenerating Cargo.lock"
  work="$workroot/cargo"
  mkdir -p "$work"
  for name in "${!repo_checkout[@]}"; do
    if [ "$name" = "proxmox-backup" ]; then
      cp -r --no-preserve=mode,ownership "${repo_checkout[$name]}" "$work/proxmox-backup"
    else
      ln -s "${repo_checkout[$name]}" "$work/$name"
    fi
  done

  patch="$(echo "$pkgdir"/*cargo-re-route-dependencies*.patch)"
  git -C "$work/proxmox-backup" apply --unsafe-paths -p1 "$patch"
  rm -f "$work/proxmox-backup/.cargo/config.toml"

  (
    cd "$work/proxmox-backup"
    # Prefer the offline crates.io index if already present, else fetch it.
    if ! cargo generate-lockfile --offline 2>/dev/null; then
      cargo generate-lockfile
    fi
  )
  cp "$work/proxmox-backup/Cargo.lock" "$pkgdir/Cargo.lock"
  echo "  Cargo.lock: updated"
fi

echo "Done."
